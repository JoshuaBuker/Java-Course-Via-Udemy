public class Application {

    public static void main(String[] args) {

        // A boolean can be declared with a conditional statement rather than just true or false
        boolean loop = 6 < 1;
        System.out.println(loop);

        int value = 0;

        // Checks the condition then runs the code. Only runs when the condition is true
        while (value < 10) {
            System.out.println("Hello");

            value++;
        }
    }
    }