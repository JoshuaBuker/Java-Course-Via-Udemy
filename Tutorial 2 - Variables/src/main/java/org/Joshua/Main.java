package org.Joshua;

// Variables are boxes of different sizes that store a value.

public class Main {
    public static void main(String[] args) {

    // All of these variables are known as primitive types, or cannot be modified
        // Integer type is a 32 bit variable
        int myNumber = 88;

        // Short type is a 16 bit variable
        short myShort = 847;

        // Long type is a 64 bit variable
        long myLong = 7797;

        // Double type is a 64 bit variable and serves the purpose of decimals
        double myDouble = 1.047;

        // Float type is a 32 bit double. Requires f at the end of the value
        float myFloat = 1.048f;

        // Char type holds a single unicode character
        char myChar = 'y';

        // boolean type (Not capitalized) is a true or false value. True by default if not specified
        boolean myBoolean = false;

        // Byte type is an 8 bit variable
        byte myByte = 127;

        System.out.println(myNumber);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBoolean);
        System.out.println(myByte);


    }
}